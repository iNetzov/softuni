public class Address {

    private String name;
    private String country;
    private String region;
    private String city;
    private String area;
    private String postalCode;
    private String email;
    private String phoneNumber;
    private String state;
    private String street;
    private String number;
    private String building;
    private String entrance;
    private String floor;
    private String apartment;

    public Address() {
    }

    public Address setName(String name) {
        this.name = name;
        return this;
    }

    public Address setCountry(String country) {
        this.country = country;
        return this;
    }

    public Address setRegion(String region) {
        this.region = region;
        return this;
    }

    public Address setCity(String city) {
        this.city = city;
        return this;
    }

    public Address setArea(String area) {
        this.area = area;
        return this;
    }

    public Address setPostalCode(String postalCode) {
        this.postalCode = postalCode;
        return this;
    }

    public Address setEmail(String email) {
        this.email = email;
        return this;
    }

    public Address setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
        return this;
    }

    public Address setState(String state) {
        this.state = state;
        return this;
    }

    public Address setStreet(String street) {
        this.street = street;
        return this;
    }

    public Address setNumber(String number) {
        this.number = number;
        return this;
    }

    public Address setBuilding(String building) {
        this.building = building;
        return this;
    }

    public Address setEntrance(String entrance) {
        this.entrance = entrance;
        return this;
    }

    public Address setFloor(String floor) {
        this.floor = floor;
        return this;
    }

    public Address setApartment(String apartment) {
        this.apartment = apartment;
        return this;
    }
}
